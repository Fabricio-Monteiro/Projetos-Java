    public void readJtableCliente(){
        DefaultTableModel modelo = (DefaultTableModel) jtbcli.getModel();
        modelo.setNumRows(0);
        ClienteDAO dao = new ClienteDAO();
        
        for(Cliente c: dao.listarTodos()){
            
            modelo.addRow(new Object[]{
                c.getIdcliente(),
                c.getNome(),
                c.getTelefone(),
                c.getEndereco(),
            });
            
        }
          
    }
    
    
    public void readJtableForDesc(String desc){
        DefaultTableModel modelo = (DefaultTableModel) jtbcli.getModel();
        modelo.setNumRows(0);
        ClienteDAO dao = new ClienteDAO();
        
        for(Cliente c: dao.buscaPorNome(desc)){
            
            modelo.addRow(new Object[]{
                c.getIdcliente(),
                c.getNome(),
                c.getTelefone(),
                c.getEndereco(),
                
            });
        }
    }
    
    
