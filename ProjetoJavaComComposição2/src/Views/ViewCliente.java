    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        if(jtbcli.getSelectedRow() != -1){
            
            
            
        
           Cliente c = new Cliente();
           c.setIdcliente(Integer.parseInt(idcli.getText()));
           ClienteDAO dao = new ClienteDAO();
           c.setIdcliente((int)jtbcli.getValueAt(jtbcli.getSelectedRow(), 0));
           c.setNome(jtnome.getText());
           c.setTelefone(jttelefone.getText());
           c.setEndereco(jtendereco.getText());
           
          dao.atualizar(c);
          
          readJtableCliente();;
        
        jtnome.setText("");
        jttelefone.setText("");
        jtendereco.setText("");
        
        
        
        JOptionPane.showMessageDialog(null, "Cliente Atualizado!");
            
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha da tabela para atualizar!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

